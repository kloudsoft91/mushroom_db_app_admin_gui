<template>
    <fieldset class="mb-2 border-1 border-slate-150 px-2" id="mush_stipe_fset">
        <legend>Stipe Features</legend>
        <label for="mush_stipe_desc">Stipe Description</label>
        <input
            type="text"
            id="mush_stipe_desc"
            class="form-input"
            v-model="stipeDescription"
            @change="addMushKey('stipe_features', stipeDescription, 'description')" />

        <label for="mush_stipe_diameter_min">Min Diameter (mm)</label>
        <input
            type="text"
            id="mush_stipe_diameter_min"
            class="form-input"
            v-model="stipeDiaMin"
            @change="addMushKey('stipe_features', parseInt(stipeDiaMin), 'diameter_min')" />

        <label for="mush_stipe_diameter_max">Max Diameter (mm)</label>
        <input
            type="text"
            id="mush_stipe_diameter_max"
            class="form-input"
            v-model="stipeDiaMax"
            @change="addMushKey('stipe_features', parseInt(stipeDiaMax), 'diameter_max')" />

        <label for="mush_stipe_colour">Stipe Colour</label>
        <select
            id="mush_stipe_colour"
            name="mush_stipe_colour"
            class="form-input"
            v-model="stipeColour"
            @change="addMushKey('stipe_features', stipeColour, 'colour')">
                <option value="white">White</option>
                <option value="red">Red</option>
                <option value="yellowish-brown">Yellowish-Brown</option>
                <option value="pink">Pink</option>
        </select>

        <label for="mush_stipe_length_min">Min Length (mm)</label>
        <input
            type="text"
            id="mush_stipe_length_min"
            class="form-input"
            v-model="stipeLenMin"
            @change="addMushKey('stipe_features', parseInt(stipeLenMin), 'length_min')" />

        <label for="mush_stipe_length_max">Max Length (mm)</label>
        <input
            type="text"
            id="mush_stipe_length_max"
            class="form-input"
            v-model="stipeLenMax"
            @change="addMushKey('stipe_features', parseInt(stipeLenMax), 'length_max')" />

        <!-- <label for="mush_stipe_height_min">Min Height (mm)</label>
        <input
            type="text"
            id="mush_stipe_height_min"
